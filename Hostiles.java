
public class Hostiles {
	private int hp;
	private int dmg;
	
	public Hostiles(int hp, int dmg) {
		this.hp = hp;
		this.dmg = dmg;
	}
	
	//=-------------------------------=//
	public int getHp() {
		return hp;
	}
	
	public void setHp(int HP) {
		hp = HP;
	}
	
	public int getDmg() {
		return dmg;
	}
	
	public void setDmg(int Dmg) {
		dmg = Dmg;
	}
}
